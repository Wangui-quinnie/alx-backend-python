#!/usr/bin/env python3
"""A function add that returns the sum of it arguments."""


def add(a: float, b: float) -> float:
    """
    Adds two floats and returns their sum.

    Args:
        a (float): The first float.
        b (float): The second float.

    Returns:
        float: The sum of a and b.
    """
    return a + b

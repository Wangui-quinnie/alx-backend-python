#!/usr/bin/env python3
"""A function to_str that takes a float n as argument and returns
the string representationof the float."""


def to_str(n: float) -> str:
    """
    Returns the string representation of a float.

    Args:
        n (float): The input float.

    Returns:
        str: The string representation of the input float.
    """
    return str(n)
